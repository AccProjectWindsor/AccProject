package com.ims.beans;

public class WorkExperienceBean {
	private int ExperCode;
	private int StuId;
	private int InternCode;
	
	public int getExperCode() {
		return ExperCode;
	}

	public void setExperCode(int ExperCode) {
		this.ExperCode = ExperCode;
	}
	
	public int getStuId() {
		return StuId;
	}

	public void setStuId(int StuId) {
		this.StuId = StuId;
	}
	
	public int getInternCode() {
		return InternCode;
	}

	public void setInternCode(int InternCode) {
		this.InternCode = InternCode;
	}
}
